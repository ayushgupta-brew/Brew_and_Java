package Basics;

//Literal in programming means a value written directly in the code, like a number, word, or true/false.
//Example: 10, "Hello", and true are literals.

public class Literals {
    public static void main(String[] args) {
        // literals
        int num = 0b101;  // binary we use b .
        int num1 = 0x7E;  // hexadecimal we use x .
        double num2 = 12e10;  // e is the constant where it denotes a scientific notation power 10 .

        System.out.println(num2);
    }
}
